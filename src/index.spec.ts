import validateNhsNumber from './index';

describe('validateNhsNumber', () => {

	it.each`
		nhsNumber
		${[0, 6, 9, 9, 8, 2, 4, 0, 2, 8]}
		${[9, 6, 8, 8, 5, 2, 6, 3, 2, 0]}
		${[1, 0, 6, 8, 0, 2, 0, 1, 6, 4]}
		${[1, 2, 0, 1, 3, 3, 8, 1, 5, 8]}
		${[9, 9, 3, 1, 8, 3, 9, 4, 7, 3]}
		${[4, 1, 2, 9, 3, 3, 1, 1, 4, 0]}
		${[1, 1, 6, 7, 9, 7, 5, 2, 7, 8]}
		${[4, 2, 9, 7, 0, 2, 9, 7, 0, 7]}
		${[9, 1, 9, 6, 6, 6, 5, 7, 7, 7]}
		${[5, 9, 1, 8, 1, 6, 8, 2, 1, 4]}
		${'9091666080'}
		${'1925612732'}
		${'9951710034'}
		${'5393923856'}
		${'1955177619'}
		${'5505704778'}
		${'651 777 4866'}
		${'7140581770   '}
		${'   7469126406'}
		${'6 9 4 5 0 9 2 4 4 5'}
		${' 7141231986 '}
		${'315142264 0'}
	`('returns true for $nhsNumber', ({ nhsNumber }) => {
		expect(validateNhsNumber(nhsNumber)).toBe(true);
	});


	it.each`
		nhsNumber
		${null}
		${undefined}
		${false}
		${{}}
		${100}
		${'udbasivhr'}
		${'86740740'}
		${'10m8652t340e7754'}
		${'1086526163407754'}
		${'********'}
		${[]}
		${[0, 0, 9, 8, 0, 0, 9, 9, null, 4]}
		${['2', '6', '1', '4', '2', '5', '7', '6', '2', '1']}
		${[4, 4, 7, 2, 6, 6]}
		${[5, 8, 3, 5, 1, 9, 4, 4, 1, 3, 8, 8, 5]}
		${[2, 88, 5, 6, 4, 5, 8, 46, 9, 1]}
		${[5, 4, 5, 8, 7, Number.NaN, 7, 9, 5, 3]}
		${[3, 0, 7, 9, 7, 7, 0, 1, 8, Number.POSITIVE_INFINITY]}
		${[4, 4, 8, 4, 7, 2, 2, 8, 6, 6]}
		${[8, 8, 6, 6, 5, 4, 6, 1, 5, 4]}
		${[6, 0, 4, 7, 3, 0, 9, 4, 7, 4]}
		${[7, 6, 0, 9, 3, 4, 2, 7, 1, 1]}
		${[7, 3, 4, 8, 1, 4, 8, 2, 5, 2]}
		${[3, 3, 5, 6, 6, 1, 6, 5, 6, 4]}
		${[0, 3, 0, 5, 6, 1, 7, 6, 1, 9]}
		${[8, 1, 6, 6, 6, 1, 7, 7, 4, 8]}
		${[6, 4, 7, 7, 6, 0, 5, 8, 1, 8]}
		${[3, 2, 8, 2, 9, 4, 8, 9, 5, 0]}
	`('returns false for $nhsNumber', ({ nhsNumber }) => {
		expect(validateNhsNumber(nhsNumber)).toBe(false);
	});

});
